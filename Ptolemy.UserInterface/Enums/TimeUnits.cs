namespace Ptolemy.UserInterface.Enums
{
    /// <summary>
    /// Enum whose value is the number of seconds per unit.
    /// </summary>
    internal enum TimeUnits
    {
        Second = 1,
        /// <summary>
        /// 60 seconds
        /// </summary>
        Minute = 60,
        /// <summary>
        /// 60 minutes
        /// </summary>
        Hour = 3600,
        /// <summary>
        /// 24 hours
        /// </summary>
        Day = 86400,
        /// <summary>
        /// 7 days
        /// </summary>
        Week = 604800,
        /// <summary>
        /// 30 days
        /// </summary>
        Month = 2592000,
        /// <summary>
        /// 365 days
        /// </summary>
        Year = 31536000
    }
}
